void delay(int i) {
    int j, k;
    for (j = 0; j < i; j++)
        for (k = 0; k < 110; k++);
}

void transfer_command(int data1) {
    char i;
    cs1 = 0;
    rs = 0;
    for (i = 0; i < 8; i++) {
        sclk = 0;
        if (data1 & 0x80) sid = 1;
        else sid = 0;
        sclk = 1;
        data1 <<= 1;
    }
    cs1 = 1;
}

void transfer_data(int data1) {
    char i;
    cs1 = 0;
    rs = 1;
    for (i = 0; i < 8; i++) {
        sclk = 0;
        if (data1 & 0x80) sid = 1;
        else sid = 0;
        sclk = 1;
        data1 <<= 1;
    }
    cs1 = 1;
}

void initial_lcd() {
    cs1 = 0;
    reset = 0;
    delay(500);
    reset = 1;
    delay(200);
    transfer_command(0xe2);
    delay(50);
    transfer_command(0x2c);
    delay(50);
    transfer_command(0x2e);
    delay(50);
    transfer_command(0x2f);
    delay(50);
    transfer_command(0x23);
    transfer_command(0x81);
    transfer_command(0x28);
    transfer_command(0xa2);
    transfer_command(0xc8);
    transfer_command(0xa0);
    transfer_command(0x40);
    transfer_command(0xaf);
}

void lcd_address(uchar page, uchar column) {
    transfer_command(0xb0 + page);
    transfer_command(((column >> 4)&0x0f) + 0x10);
    transfer_command(column & 0x0f);
}

void clear_screen() {
    uchar i, j;
    for (i = 0; i < 9; i++) {
        lcd_address(i, 0);
        for (j = 0; j < 132; j++) {
            transfer_data(0x00);
        }
    }
}

void clear_screen_64() {
    uchar i, j;
    for (i = 0; i < 9; i++) {
        lcd_address(i, 33);
        for (j = 0; j < 64; j++) {
            transfer_data(0x00);
        }
    }
}

void disp(uchar page, uchar column, uchar *dp, int width, int height) {
    uchar i, j, k, *data_temp, data1;
    data_temp = dp;
    for (j = 0; j < width/8; j++) {
        lcd_address(page + j, column);
        for (i = 0; i < height; i++) {
            transfer_data(*data_temp);
            data_temp++;
        }
    }
}

